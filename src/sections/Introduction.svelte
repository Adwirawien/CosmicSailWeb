    <p class="text-sm">
      A while ago I found an old rusty rc sailboat on my attic without any controls or batteries.
      So I decided to burn my money with making this boat as cool and advanced as possible...<br/><br/>

      At first I went over a few ordinary systems with radio control,
      but I noticed very quickly that the problem with those radio controls is that they lose signal after about 50
      meters.
      In my case that may was related to the antennas being located somewhat below the waterline, blocking the
      signal.<br/><br/>

      That was a bit disappointing but nevertheless I brainstormed for a better solution and
      came up with the idea of controlling the boat and its motors with a Raspberrypi.</p>
